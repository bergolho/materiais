#include <stdio.h>
#include <iostream>
#include <string>
using namespace std;

/* +------------------------+
|#.#.#.#.#.#.#.#.#.#.#.|D|)
|#.#.#.#.#.#.#.#.#.#.#.|.|
|#.......................|
|#.#.#.#.#.#.#.#.#.#.#.|.|)
+------------------------+ */

string a[10];

int main(void) {
    int k;

    int i, j;

    a[0] = "+------------------------+";
    a[1] = "|#.#.#.#.#.#.#.#.#.#.#.|D|)";
    a[2] = "|#.#.#.#.#.#.#.#.#.#.#.|.|";
    a[3] = "|#.......................|";
    a[4] = "|#.#.#.#.#.#.#.#.#.#.#.|.|)";
    a[5] = "+------------------------+";

    scanf(" %d", &k);

    for (j = 0; j <= (int)a[0].size(); j++) {
        for (i = 0; i <= 5; i++) {
            if (k > 0 && a[i][j] == '#') {
                a[i][j] = 'O';
                k--;
            }
        }
    }

    for (i = 0; i <= 5; i++) {
        cout << a[i] << endl;
    }
    return 0;
}
